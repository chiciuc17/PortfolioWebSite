document.addEventListener('DOMContentLoaded', () => {
    // --- Language Switcher Logic ---
    const currentLangSpan = document.querySelector('.current-lang');
    const langItems = document.querySelectorAll('.lang-menu li');

    // Function to retrieve nested values from object
    function getNestedTranslation(obj, path) {
        return path.split('.').reduce((prev, curr) => {
            return prev ? prev[curr] : null;
        }, obj);
    }

    function changeLanguage(lang) {
        // Validation
        if (!translations[lang]) {
            console.error('Language not found:', lang);
            return;
        }

        // Save to local storage
        localStorage.setItem('preferredLang', lang);

        // Update UI Text
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = getNestedTranslation(translations[lang], key);
            if (translation) {
                element.textContent = translation;
            }
        });

        // Update Switcher Display
        const langNames = {
            en: 'EN',
            de: 'DE',
            ro: 'RO',
            ru: 'RU'
        };
        const langFlags = {
            en: 'gb',
            de: 'de',
            ro: 'ro',
            ru: 'ru'
        };
        if (currentLangSpan) {
            currentLangSpan.innerHTML = `<span class="fi fi-${langFlags[lang]}"></span> ${langNames[lang]} <i class="fas fa-chevron-down"></i>`;
        }

        // Update document language attribute
        document.documentElement.lang = lang;
    }

    // Initialize Language
    const savedLang = localStorage.getItem('preferredLang') || 'en';
    changeLanguage(savedLang);

    // Event Listeners for Switcher
    langItems.forEach(item => {
        item.addEventListener('click', () => {
            const lang = item.getAttribute('data-lang');
            changeLanguage(lang);
        });
    });
    // --- End Language Switcher Logic ---

    // Intersection Observer for Fade-in animations
    const observerOptions = {
        threshold: 0.1,
        rootMargin: "0px 0px -50px 0px"
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                // Optional: Stop observing once visible to run only once
                // observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    const fadeElements = document.querySelectorAll('.fade-in-up');
    fadeElements.forEach(el => observer.observe(el));

    // Smooth Scrolling for Navigation Links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });

    // Mobile Menu Toggle (Basic implementation)
    const hamburger = document.querySelector('.hamburger');
    const navLinks = document.querySelector('.nav-links');

    if (hamburger) {
        hamburger.addEventListener('click', () => {
            navLinks.style.display = navLinks.style.display === 'flex' ? 'none' : 'flex';
            if (navLinks.style.display === 'flex') {
                navLinks.style.flexDirection = 'column';
                navLinks.style.position = 'absolute';
                navLinks.style.top = '100%';
                navLinks.style.left = '0';
                navLinks.style.width = '100%';
                navLinks.style.background = 'rgba(10, 10, 10, 0.95)';
                navLinks.style.padding = '1rem 0';
                navLinks.style.borderBottom = '1px solid rgba(255,255,255,0.1)';
            }
        });
    }
});
